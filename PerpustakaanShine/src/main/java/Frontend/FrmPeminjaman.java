    private void btnTambahBaruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahBaruActionPerformed
        // TODO add your handling code here:
        Peminjaman p = new Peminjaman();
        p.setIdpeminjaman(Integer.parseInt(txtId.getText()));
        p.getAnggota().setIdanggota(Integer.parseInt(txtIdAng.getText()));
        p.getBuku().setIdbuku(Integer.parseInt(txtIdBuku.getText()));
        p.setTanggalpinjam(txtTglP.getText());
        p.setTanggalkembali(txtTglK.getText());
        p.save();
        
        txtId.setText(Integer.toString(p.getIdpeminjaman()));
        
        tampilkanData();
    }//GEN-LAST:event_btnTambahBaruActionPerformed

    private void tblPinjamMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPinjamMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblPinjam.getModel();
        int row = tblPinjam.getSelectedRow();
        Peminjaman pinjam = new Peminjaman();
        
        pinjam = new Peminjaman().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtId.setText(String.valueOf(pinjam.getIdpeminjaman()));
        txtIdAng.setText(String.valueOf(pinjam.getAnggota().getIdanggota()));
        txtIdBuku.setText(String.valueOf(pinjam.getBuku().getIdbuku()));
        txtTglP.setText(pinjam.getTanggalpinjam());
        txtTglK.setText(pinjam.getTanggalkembali());
    }//GEN-LAST:event_tblPinjamMouseClicked

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblPinjam.getModel();
        int row = tblPinjam.getSelectedRow();
        
        Buku buku = new Buku().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        buku.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

