    public void cari(String keyword) {
        String[] kolom = {"ID", "ID Anggota", "ID Buku", "Tanggal Pinjam", "Tanggal Kembali"};
        ArrayList<Peminjaman> list = new Peminjaman().getAll();
        Object rowData[] = new Object[5];

        tblPinjam.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (Peminjaman pjm : list) {
            rowData[0] = pjm.getIdpeminjaman();
            rowData[1] = pjm.getAnggota().getNama();
            rowData[2] = pjm.getBuku().getJudul();
            rowData[3] = pjm.getTanggalpinjam();
            rowData[4] = pjm.getTanggalkembali();

            ((DefaultTableModel) tblPinjam.getModel()).addRow(rowData);

        }
    }
    public final void tampilkanData() {
        String[] kolom = {"ID", "ID Anggota", "ID Buku", "Tanggal Pinjam", "Tanggal Kembali"};
        ArrayList<Peminjaman> p = new Peminjaman().getAll();
        Object rowData[] = new Object[5];

        tblPinjam.setModel(new DefaultTableModel(new Object[][]{}, kolom));

        for (int i = 0; i < p.size(); i++) {
            rowData[0] = p.get(i).getIdpeminjaman();
            rowData[1] = p.get(i).getAnggota().getNama();
            rowData[2] = p.get(i).getBuku().getJudul();
            rowData[3] = p.get(i).getTanggalpinjam();
            rowData[4] = p.get(i).getTanggalkembali();

            ((DefaultTableModel) tblPinjam.getModel()).addRow(rowData);
        }
    }
    
